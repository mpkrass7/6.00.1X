def search3(L, e):
    if L[0] == e:
        return True
    elif L[0] > e:
        return False
    else:
         try:
             return search3(L[1:], e)
         except IndexError:
             return False

def swapSort(L): 
    """ L is a list on integers """
    print("Original L: ", L)
    for i in range(len(L)):
        for j in range(i+1, len(L)):
            if L[j] < L[i]:
                # the next line is a short 
                # form for swap L[i] and L[j]
                L[j], L[i] = L[i], L[j] 
                print(L)
    print("Final L: ", L)


